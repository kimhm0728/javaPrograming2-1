package week04;

public class RelationalOPTest {

	public static void main(String[] args) {
		byte a=10;
		double d=3.1415;
		
		System.out.println("a가 d보다 큰가? "+(a>d));
		System.out.println("a가 10.0f와 같은가? "+(a==10.0f));
		System.out.println("10이 10과 같지 않은가? "+(10!=10.0));
		System.out.println("10이 20보다 작거나 같은가? "+(10<=20));
		System.out.println("10이 20보다 작은가? "+(10<20));
		System.out.println("10이 20보다 크거나 같은가? "+(10>=20));

	}

}
