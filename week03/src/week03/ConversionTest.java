package week03;

public class ConversionTest {

	public static void main(String[] args) {
		int i1=100; // 10진수
		int i2=0b100; // 2진수
		int i3=0100; // 8진수
		int i4=0x100; // 16진수
		
		System.out.println("10진수 100 = "+ i1);
		System.out.println("2진수 0b100 = "+ i2);
		System.out.println("8진수 0100 = "+i3);
		System.out.println("16진수 0x100 = "+i4);
	}

}
